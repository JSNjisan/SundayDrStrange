package com.upskill.Java_1;

public class AccessModifier {

	// public - Any class can access
	
	// private - only same class can access
	
	// protected - classes in the same package and subclass
	
	// default - classes in the same package can access 
	
	public String name = "Halalguy"; // public - Any class can access

    private int age = 19;           // private - only same class can access
	
	protected int ssn = 911311;     // protected - classes in the same package and subclass
	
	String address = "Haram Tower";  // defaul  - classes in the same package can access 
	
	
	

}
